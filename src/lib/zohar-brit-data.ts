// Build COMPLET = ouverture + extra + version courte (sauf pétihah) + clôture
const COMPLET: ZoharSection[] = (() => {
  const arr: ZoharSection[] = [];
  arr.push(COURT[0]); // pétihah
  EXTRA.slice(0, 5).forEach((s) => arr.push(s));   // Lekh Lekha extras
  COURT.slice(1, 5).forEach((s) => arr.push(s));   // 4 sections Lekh Lekha de COURT
  EXTRA.slice(5).forEach((s) => arr.push(s));      // Vayera extras
  COURT.slice(5, 9).forEach((s) => arr.push(s));   // 4 sections Vayera de COURT
  arr.push(COURT[9]);                              // tefilah finale
  return arr.map((s, i) => ({ ...s, index: i }));
})();

export const ZOHAR_BRIT: Record<ZoharVersion, ZoharSection[]> = {
  court: COURT,
  complet: COMPLET,
};

export function getZoharSections(version: ZoharVersion): ZoharSection[] {
  return ZOHAR_BRIT[version];
}

export function sectionWeight(s: ZoharSection): number {
  return s.paragraphs.reduce((acc, p) => acc + p.length, 0);
}
